<script>
import $ from 'jquery'
export default {
    props: ['user'],
    data: () => {
        return {
            table: false,
            buys: {
                bet: 1,
                type: 100
            },
            game: {
                spins: 0,
                coffs: 0,
                active: false,
                win: 0,
                width: 0
            },
            animate: {
                tubeUp: false,
                tube: [
                    { id: 1, color: 'red' },
                    { id: 2, color: 'blue' },
                    { id: 3, color: 'blue' },
                    { id: 4, color: 'red' },
                    { id: 5, color: 'blue' },
                    { id: 6, color: 'blue' }]
            }
        }
    },
    methods: {
        start() {
            this.table = false
            this.animate.tubeUp = true
            this.game.active = true
            const width = $('.monetrainCells').width()
            this.game.right = width
            $(`.leftsInbox`).css({ 'transform': 'translateX(-' + width + 'px)' })
            setTimeout(() => {
                this.animate.tube.forEach((e) => {
                    e.color = this.getRandom(0, 1) ? 'red' : 'blue'
                    return e.coffs = this.getRandom(0, 1) ? Math.floor(this.getRandom(1, 100) / 10) : false
                })
            }, 1300)
            setTimeout(() => {
                this.animate.tubeUp = false
            }, 2000)
        },
        getRandom(min, max) {
            return Math.floor(Math.random() * (max - min + 1) + min)
        },
        shuffle(arr) {
            var j, temp;
            for (var i = arr.length - 1; i > 0; i--) {
                j = Math.floor(Math.random() * (i + 1));
                temp = arr[j];
                arr[j] = arr[i];
                arr[i] = temp;
            }
            return arr;
        }
    },
    computed: {
        buysBet() {
            return this.buys.bet
        },
    },
    watch: {
        buysBet() {
            if (this.buysBet < 1) return this.buys.bet = 1
            if (this.buysBet > 100) return this.buys.bet = 100
        }
    }
}
</script>
